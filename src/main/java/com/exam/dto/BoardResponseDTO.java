package com.exam.dto;

public class BoardResponseDTO {

    private Long id;
    private String name;

    public BoardResponseDTO() {
    }

    public BoardResponseDTO(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}